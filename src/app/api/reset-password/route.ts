const resend = new Resend('re_QUgC44fY_KeqwyBijtkS1GzMnriRXh5pT');

export async function POST(req: NextRequest) {
    try {
        const { email } = await req.json();

        if (!email) throw { status: 404, message: "Please Provide Email" }

        const user = await prisma.user.findUnique({ where: { email } })

        if (!user) throw { state: 404, message: "User doesn't exists" }

        const token = await signToken({
            email: user.email
        })

        await prisma.token.upsert({
            create: {
                email: user.email,
                token,
            },
            update: {
                token,
            },
            where: {
                email
            }
        })

        const res = await resend.emails.send({
            from: 'CryptoInvestUSA<no-reply@mail.thequotesgram.com>',
            to: user.email,
            subject: 'Crypto Invest Account Password Reset',
            html: html({ name: `${user.fname} ${user.lname}`, token })
        });

        return NextResponse.json({ res, message: "Mail Sent" }, { status: 200 })

    } catch (err: any) {
        return NextResponse.json({ message: err.message || JSON.stringify(err) || "Internal Error" }, { status: err.status || 500 })
    }
}